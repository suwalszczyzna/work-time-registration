import os

from django.conf import settings
from django.contrib.staticfiles import finders
from django.http import HttpResponse
from django.template.loader import get_template
from xhtml2pdf import pisa


class PdfGenerator:
    def __init__(self, filename: str, template_path: str):
        self.filename = filename
        self.template_path = template_path

    def generate(self, context: dict):
        response = self._create_response()
        template = get_template(self.template_path)
        html = template.render(context)

        pdf_status = pisa.CreatePDF(
            html,
            dest=response,
            encoding='utf-8',
            link_callback=self._link_callback
        )
        if pdf_status.err:
            return HttpResponse('Problem z wygenerowaniem PDF')
        return response

    def _create_response(self):
        response = HttpResponse(content_type='application/pdf')
        response['Content-Disposition'] = 'filename="{}.pdf"'.format(self.filename)
        return response

    @staticmethod
    def _link_callback(uri, rel):
        """
        Convert HTML URIs to absolute system paths so xhtml2pdf can access those
        resources
        """
        result = finders.find(uri)
        if result:
            if not isinstance(result, (list, tuple)):
                result = [result]
            result = list(os.path.realpath(path) for path in result)
            path = result[0]
        else:
            static_url = settings.STATIC_URL  # Typically /static/
            static_root = settings.STATIC_ROOT  # Typically /home/userX/project_static/
            media_url = settings.MEDIA_URL  # Typically /media/
            media_root = settings.MEDIA_ROOT  # Typically /home/userX/project_static/media/

            if uri.startswith(media_url):
                path = os.path.join(media_root, uri.replace(media_url, ""))
            elif uri.startswith(static_url):
                path = os.path.join(static_root, uri.replace(static_url, ""))
            else:
                return uri

            # make sure that file exists
            if not os.path.isfile(path):
                raise LookupError(
                    'media URI must start with %s or %s' % (static_url, media_url)
                )
        return path


